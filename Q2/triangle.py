'''
เส้นรอบวง(p) P = a + b + c
สมการสามเหลี่ยม c^2 = a^2 + b^2 -> b^2 = a^2 - c^2
เเทนค่า b^2 ในสมการเเรก จะได้สมการค่า c ที่มีตัวแปร p เเละ a
ลูปหาค่า a ที่ทำให้ c เป็นจำนวนเต็ม เเละมากที่สุด O(n)
'''

p = int(input())
max_c = 0

for x in range(1, p // 2): # เดาว่าเลขไม่น่าเกิน p / 2
  c = ( (p-x)**2 + x**2 ) / (2 * (p-x))
  if c % 1 == 0 and c > max_c:
      max_c = c

print(int(max_c))